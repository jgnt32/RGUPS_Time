package ru.rgups.time.model;

import java.util.Date;

public class CalendarListElement{
	
	private Date date;
	
	private int lineSide;

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public int getLineSide() {
		return lineSide;
	}

	public void setLineSide(int lineSide) {
		this.lineSide = lineSide;
	}
	
}